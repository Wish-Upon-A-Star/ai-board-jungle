import { existsSync, mkdirSync, writeFileSync } from "node:fs";
import { join } from "node:path";

const tunnelName = process.env.AI_BOARD_CLOUDFLARE_TUNNEL_NAME || "ai-board";
const hostname = process.env.AI_BOARD_CLOUDFLARE_HOSTNAME || "ai-board.example.com";
const port = process.env.AI_BOARD_EXTERNAL_PORT || "8130";
const configDir = process.env.AI_BOARD_CLOUDFLARE_CONFIG_DIR || join("data", "cloudflare");
const configPath = join(configDir, "config.yml");
const localUrl = `http://127.0.0.1:${port}`;

mkdirSync(configDir, { recursive: true });

const config = [
  `# Generated by npm run setup:cloudflare`,
  `# Replace <TUNNEL-UUID> after running: cloudflared tunnel create ${tunnelName}`,
  `tunnel: <TUNNEL-UUID>`,
  `credentials-file: ${configDir.replaceAll("\\", "/")}/<TUNNEL-UUID>.json`,
  "",
  "ingress:",
  `  - hostname: ${hostname}`,
  `    service: ${localUrl}`,
  "  - service: http_status:404",
  "",
].join("\n");

if (!existsSync(configPath) || process.argv.includes("--force")) {
  writeFileSync(configPath, config, "utf8");
}

const callbacks = {
  github: `https://${hostname}/api/oauth/github/callback`,
  notion: `https://${hostname}/api/oauth/notion/callback`,
  figma: `https://${hostname}/api/oauth/figma/callback`,
  google_calendar: `https://${hostname}/api/oauth/google_calendar/callback`,
};

console.log(JSON.stringify({
  ok: true,
  tunnelName,
  hostname,
  localUrl,
  configPath,
  nextCommands: [
    "cloudflared tunnel login",
    `cloudflared tunnel create ${tunnelName}`,
    `cloudflared tunnel route dns ${tunnelName} ${hostname}`,
    `set AI_BOARD_PUBLIC_BASE_URL=https://${hostname}`,
    "npm run serve:external -- --named-tunnel",
  ],
  callbacks,
}, null, 2));
